class Solution(object):
    def twoSum(self, nums, target):
        dic = {}
        
        for index,ele in enumerate(nums):
            
            if target-ele not in dic:
                dic[ele] = index
                
            else:
                return index,dic[target-ele]
                
            
            
            
            
                
                
                
        
        
                
                
                
                
                
                
                
                
                
        
        
        
        




        